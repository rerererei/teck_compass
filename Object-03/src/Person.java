
public class Person {

	/*-----------------------
	 * フィールド
	 *-----------------------*/
	// 名前
	private String name;
	// 年齢
	private int age;

	/*-----------------------
	 * コンストラクタ
	 *-----------------------*/

	/*-------------------------
	 * メソッド
	 *-------------------------*/
	// nameのgetter
	public String getName() {
		return this.name;
	}

	// nameのgetter
	public void setName(String name) {
		this.name = name;
	}

	// ageのsetter
	public int getAge() {
		return this.age;
	}

	// ageのgetter
	public void setAge(int age) {
		this.age = age;
	}
}
